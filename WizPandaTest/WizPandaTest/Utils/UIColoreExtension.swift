import Foundation
import UIKit

extension UIColor {
    
  @nonobjc class var blueberry: UIColor {
    return UIColor(red: 76.0 / 255.0, green: 64.0 / 255.0, blue: 170.0 / 255.0, alpha: 1.0)
  }
    
  @nonobjc class var black: UIColor {
    return UIColor(white: 0.0, alpha: 1.0)
  }
    
  @nonobjc class var dodgerBlue: UIColor {
    return UIColor(red: 51.0 / 255.0, green: 118.0 / 255.0, blue: 246.0 / 255.0, alpha: 1.0)
  }
}
